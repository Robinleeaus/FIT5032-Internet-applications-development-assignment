        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult UploadMemberImage([Bind(Include = "Id,Email,EmailConfirmed,PasswordHash,SecurityStamp,PhoneNumber,PhoneNumberConfirmed,TwoFactorEnabled,LockoutEndDateUtc,LockoutEnabled,AccessFailedCount,UserName,Address,Image,Dob,FirstName,LastName")] AspNetUser aspNetUser, HttpPostedFileBase postedFile)
        {
            //Robin add
            if (postedFile != null)
            {
                string path = Server.MapPath("~/Uploads/");
                if (!Directory.Exists(path))
                {
                    Directory.CreateDirectory(path);
                }

                if (Helpers.Util.IsRecognisedImageFile(postedFile.FileName))
                {
                    String filePath = Helpers.Util.GenerateUniqueString();
                    filePath = Helpers.Util.CalculateMD5Hash(filePath);

                    String fileExtension = Path.GetExtension(postedFile.FileName);
                    String full = filePath + fileExtension;
                    postedFile.SaveAs(path + full);
                    aspNetUser.Image = full;
                }
                else
                {
                    ViewBag.Message = "Please upload a valid image.";
                    return View(aspNetUser);
                }
            }


            if (ModelState.IsValid)
            {
                db.Entry(aspNetUser).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            return View(aspNetUser);
        }

